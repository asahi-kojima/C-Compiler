#比較演算子のテスト
assert 1 "main(){1 == 1;}"
assert 0 "main(){1 == 0;}"
assert 1 "main(){1 != 0;}"
assert 0 "main(){1 != 1;}"

assert 1 "main(){(2 * 2) == (2 + 2);}"
assert 0 "main(){2 == (1 / 1);}"
assert 1 "main(){(2 * 2) != 0;}"
assert 0 "main(){(3 * 3) != (3 + 3 + 3);}"

assert 1 "main(){0 < 1;}"
assert 1 "main(){0 <= 1;}"
assert 0 "main(){1 < 1;}"
assert 1 "main(){1 <= 1;}"
assert 0 "main(){2 < 1;}"
assert 0 "main(){2 <= 1;}"

assert 1 "main(){-1 < -0;}"
assert 1 "main(){-1 <= -0;}"
assert 0 "main(){-1 < -1;}"
assert 1 "main(){-1 <= -1;}"
assert 0 "main(){-1 < -2;}"
assert 0 "main(){-1 <= -2;}"

assert 1 "main(){1 > 0;}"
assert 1 "main(){1 >= 0;}"
assert 0 "main(){1 > 1;}"
assert 1 "main(){1 >= 1;}"
assert 0 "main(){1 > 2;}"
assert 0 "main(){1 >= 2;}"

assert 1 "main(){-0 > -1;}"
assert 1 "main(){-0 >= -1;}"
assert 1 "main(){-1 >= -1;}"
assert 0 "main(){-2 > -1;}"
assert 0 "main(){-2 >= -1;}"